#include "operations.h"
/**
 *add - add two numbers
 *@a: first number
 *@b: second number
 *Return: 1
 */
int add(int a, int b)
{
    return (a + b);
}
/**
 *sub - subtract two numbers
 *@a: first number
 *@b: second number
 *Return: 1
 */
int sub(int a, int b)
{
    return (a - b);
}
/**
 *mul - multiply two numbers
 *@a: first number
 *@b: second number
 *Return: 1
 */
int mul(int a, int b)
{
    return (a * b);
}
/**
 *div - divide two numbers
 *@a: first number
 *@b: second number
 *Return: 1
 */
int div(int a, int b)
{
    return (a / b);
}
/**
 *mod - remainder from division of two numbers
 *@a: first number
 *@b: second number
 *Return: 1
 */
int mod(int a, int b)
{
    return (a % b);
}