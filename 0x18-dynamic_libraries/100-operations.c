#include "operations.h"
/**
* add - add two numbers
* sub - subtract two numbers
* mul - multiply two numbers
* div - divide two numbers
* mod - remainder from the divivsion of two numbers
* @a: first number
* @b: second number
*/

int add(int a,int b) 
{ 
    return (a + b); 
} 
int sub(int a, int b) 
{ 
    return (a - b); 
} 
int mul(int a, int b) 
{ 
    return (a * b); 
} 
int div(int a, int b) 
{ 
    return (a / b); 
} 
int mod(int a, int b) 
{ 
    return (a % b); 
}
