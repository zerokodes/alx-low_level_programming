#include "operations.h"
/**
* add - add two numbers
* @a: first number
* @b: second number
*/
int add(int a,int b) 
{ 
    return (a + b); 
}

/**
* sub - subtract two numbers
* @a: first number
* @b: second number
*/
int sub(int a, int b) 
{ 
    return (a - b); 
}

/**
* mul - multiply two numbers
* @a: first number
* @b: second number
*/ 
int mul(int a, int b) 
{ 
    return (a * b); 
}

/**
* div - divide two numbers
* @a: first number
* @b: second number
*/ 
int div(int a, int b) 
{ 
    return (a / b); 
}

/**
* mod - remainder from division of two numbers
* @a: first number
* @b: second number
*/ 
int mod(int a, int b) 
{ 
    return (a % b); 
}
